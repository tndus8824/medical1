# 돈을 입력받아  5만원권 만원권 천원권 교환해서 출력

money=int(input("교환 할 돈이 얼마 인가요?  >>> "))
c50000=money//50000
c10000=(money%50000)//10000
c1000=(money%10000)//1000

print("50,000원권  {}장".format(c50000))
print("10,000원권  {}장".format(c10000))
print("1000원권  {}장".format(c1000))

